// Introduction to Java
// Java is a high-level, object-oriented, and platform-independent programming language.

public class Introduction {

    public static void main(String[] args) {

        // Displaying basic information about Java
        System.out.println("Welcome to Java Programming!");
        System.out.println("Java is a high-level programming language.");
        System.out.println("Java follows the Object-Oriented Programming (OOP) concept.");
        System.out.println("It is platform-independent because of the JVM.");
        System.out.println("Java follows the principle: Write Once, Run Anywhere (WORA).");
    }
}

/*
==========================================
Explanation

1. public
   - Makes the class accessible from anywhere.

2. class
   - Used to define a class.

3. Introduction
   - Name of the class.
   - The filename must also be Introduction.java.

4. main()
   - The starting point of every Java program.

5. System.out.println()
   - Prints the given text and moves the cursor to the next line.

6. WORA
   - Write Once, Run Anywhere.
   - Java programs can run on any system that has JVM.

==========================================
*/